// Client-side export helpers for IntelBot answers. Called only from event
// handlers in the browser, so DOM/Blob/window usage is safe here.

function slug(s: string): string {
  return (s || "intelbot-answer")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 48);
}

function triggerDownload(filename: string, content: string, mime: string): void {
  const blob = new Blob([content], { type: mime });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  a.remove();
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}

export function downloadMarkdown(content: string, title: string): void {
  triggerDownload(`${slug(title)}.md`, content, "text/markdown;charset=utf-8");
}

// Extract every GFM table from the markdown and emit CSV (tables separated by a
// blank line). Returns null if the answer contains no tables.
export function markdownTablesToCsv(md: string): string | null {
  const lines = md.split(/\r?\n/);
  const isRow = (l: string) => /^\s*\|.*\|\s*$/.test(l);
  const isSep = (l: string) => /^\s*\|?[\s:-]*\|[\s:|-]*$/.test(l) && l.includes("-");

  const cells = (l: string) =>
    l
      .trim()
      .replace(/^\||\|$/g, "")
      .split("|")
      .map((c) => c.trim());

  const csvCell = (c: string) =>
    /[",\n]/.test(c) ? `"${c.replace(/"/g, '""')}"` : c;

  const tables: string[] = [];
  let i = 0;
  while (i < lines.length) {
    if (isRow(lines[i]) && i + 1 < lines.length && isSep(lines[i + 1])) {
      const out: string[] = [cells(lines[i]).map(csvCell).join(",")];
      i += 2; // skip header + separator
      while (i < lines.length && isRow(lines[i])) {
        out.push(cells(lines[i]).map(csvCell).join(","));
        i++;
      }
      tables.push(out.join("\n"));
    } else {
      i++;
    }
  }
  return tables.length ? tables.join("\n\n") : null;
}

export function downloadCsv(md: string, title: string): boolean {
  const csv = markdownTablesToCsv(md);
  if (!csv) return false;
  triggerDownload(`${slug(title)}.csv`, csv, "text/csv;charset=utf-8");
  return true;
}

