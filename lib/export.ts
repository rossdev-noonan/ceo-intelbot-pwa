// Open a print-ready window from the already-rendered answer HTML; the user
// chooses "Save as PDF". Produces a clean, selectable-text document.
export function printAnswer(innerHtml: string, title: string, question?: string): void {
  const w = window.open("", "_blank", "width=860,height=900");
  if (!w) return;
  const esc = (s: string) =>
    s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
  w.document.write(
    `<!doctype html><html><head><meta charset="utf-8"><title>${esc(title)}</title>` +
      `<style>${PRINT_CSS}</style></head><body>` +
      `<h1 class="doc-title">IntelBot — Noonan</h1>` +
      `<div class="meta">${question ? esc(question) + " · " : ""}${new Date().toLocaleString()}</div>` +
      `<div class="content">${innerHtml}</div>` +
      `<div class="foot">Generated by IntelBot · grounded in Noonan's knowledge base · general guidance, not legal advice.</div>` +
      `</body></html>`
  );
  w.document.close();
  w.focus();
  setTimeout(() => w.print(), 350);
}
