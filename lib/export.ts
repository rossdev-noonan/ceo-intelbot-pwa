function escapeHtml(s: string): string {
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

function htmlShell(innerHtml: string, title: string, question?: string): string {
  return (
    `<!doctype html><html><head><meta charset="utf-8"><title>${escapeHtml(title)}</title>` +
    `<style>${PRINT_CSS}</style></head><body>` +
    `<h1 class="doc-title">IntelBot — Noonan</h1>` +
    `<div class="meta">${question ? escapeHtml(question) + " · " : ""}${new Date().toLocaleString()}</div>` +
    `<div class="content">${innerHtml}</div>` +
    `<div class="foot">Generated by IntelBot · general guidance, not legal advice.</div>` +
    `</body></html>`
  );
}

// Standalone HTML file.
export function downloadHtml(innerHtml: string, title: string, question?: string): void {
  triggerDownload(`${slug(title)}.html`, htmlShell(innerHtml, title, question), "text/html;charset=utf-8");
}

// Word document — Word opens HTML with an Office namespace header; .doc + msword MIME.
export function downloadWord(innerHtml: string, title: string, question?: string): void {
  const doc =
    `<html xmlns:o='urn:schemas-microsoft-com:office:office' xmlns:w='urn:schemas-microsoft-com:office:word' ` +
    `xmlns='http://www.w3.org/TR/REC-html40'><head><meta charset="utf-8"><title>${escapeHtml(title)}</title>` +
    `<style>${PRINT_CSS}</style></head><body>` +
    `<h1>IntelBot — Noonan</h1>` +
    `<p style="color:#666">${question ? escapeHtml(question) + " · " : ""}${new Date().toLocaleString()}</p>` +
    `${innerHtml}</body></html>`;
  triggerDownload(`${slug(title)}.doc`, doc, "application/msword");
}

// Excel — exports the answer's tables. Excel opens an HTML table workbook (.xls).
export function downloadExcel(tablesHtml: string[], title: string): boolean {
  if (!tablesHtml.length) return false;
  const doc =
    `<html xmlns:o='urn:schemas-microsoft-com:office:office' xmlns:x='urn:schemas-microsoft-com:office:excel' ` +
    `xmlns='http://www.w3.org/TR/REC-html40'><head><meta charset="utf-8">` +
    `<!--[if gte mso 9]><xml><x:ExcelWorkbook><x:ExcelWorksheets></x:ExcelWorksheets></x:ExcelWorkbook></xml><![endif]-->` +
    `</head><body>${tablesHtml.join("<br/>")}</body></html>`;
  triggerDownload(`${slug(title)}.xls`, doc, "application/vnd.ms-excel");
  return true;
}

// Plain text.
export function downloadText(text: string, title: string): void {
  triggerDownload(`${slug(title)}.txt`, text, "text/plain;charset=utf-8");
}

// Structured JSON.
export function downloadJson(answer: string, title: string, question?: string): void {
  const payload = { question: question ?? null, answer, generatedAt: new Date().toISOString() };
  triggerDownload(`${slug(title)}.json`, JSON.stringify(payload, null, 2), "application/json;charset=utf-8");
}

